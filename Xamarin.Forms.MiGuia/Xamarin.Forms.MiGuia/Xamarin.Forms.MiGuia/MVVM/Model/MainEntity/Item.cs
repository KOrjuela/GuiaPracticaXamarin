namespace Xamarin.Forms.MiGuia.MVVM.Model.MainEntity
{
    /// <summary>
    /// Representa la unidad de una colección.
    /// </summary>
    public class Item
    {
        /// <summary>
        /// Obtiene o establece el identificador del Item
        /// </summary>
        public string Id { get; set; }

        /// <summary>
        /// Obtiene o establece el nombre del Item
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Obtiene o establece una descripcion del Item
        /// </summary>
        public string Description { get; set; }

    }
}
